// Add underscored name and media links to dictionary
var addDataToDictionary = function() {
  for (var key in dictionary) {
    dictionary[key].underscoredName = dictionary[key].name.replace(/ /g, "_");
    dictionary[key].media = "assets/media/" + dictionary[key].tags[0] + "/" + dictionary[key].underscoredName + ".mp4";
  }
};


var generateSearchList = function() {
  var list = [];
  for (var key in dictionary) {
    list.push(dictionary[key].name);;
  }
  return list;
};

